package com.makeurpicks.domain;

public class View {

	private LeagueView leagueView;
	private PlayerView playerView;
	private SeasonView seasonView;
	public LeagueView getLeagueView() {
		return leagueView;
	}
	public void setLeagueView(LeagueView leagueView) {
		this.leagueView = leagueView;
	}
	public PlayerView getPlayerView() {
		return playerView;
	}
	public void setPlayerView(PlayerView playerView) {
		this.playerView = playerView;
	}
	public SeasonView getSeasonView() {
		return seasonView;
	}
	public void setSeasonView(SeasonView seasonView) {
		this.seasonView = seasonView;
	}
	
	
}
